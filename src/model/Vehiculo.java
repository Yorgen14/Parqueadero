package model;

/**
 *
 * @author estudiante
 */
public class Vehiculo {
    
    private String placa;
    private String tipo;  
    private int modelo;
    private double precio;

    public Vehiculo() {
    }

    public Vehiculo(String placa, String tipo, int modelo, double precio) {
        this.placa = placa;
        this.tipo = tipo;
        this.modelo = modelo;
        this.precio = precio;
    }

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public int getModelo() {
        return modelo;
    }

    public void setModelo(int modelo) {
        this.modelo = modelo;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }
    
    
    
}
